<h4>Wood Types</h4>
<p><strong>Poplar </strong>- We generally use poplar for standard custom drawer inserts.  Poplar is a hardwood with yellow undertones and often has pale green streaks running  through it; less often it has very dark brown streaks; rarely it will  have a flaming purple streak.  All but one of the <a href="http://orderlydrawer.com/the-custom">custom insert photos</a> on this site is made of poplar.  The image below shows the typical color variation seen in poplar.</p>
<p style="text-align: center;"><a href="http://orderlydrawer.com/wp-content/uploads/2010/06/dsc07332.jpg"><img class="size-full wp-image-1562 aligncenter" title="poplar" src="http://orderlydrawer.com/wp-content/uploads/2010/06/dsc07332.jpg" alt="" width="480" height="360" /></a></p>
<p><strong>Maple </strong>- We offer maple at a 20% increase over poplar, and it is the only wood choice for our taller organizers.  Maple has a more consistent color.  It is light in color with pink undertones; it is harder and heavier than poplar.  The <a href="http://orderlydrawer.com/the-custom-jr">photos of the Custom Jr.</a> are made of maple.  The image below shows the typical color variation seen in maple.</p>

