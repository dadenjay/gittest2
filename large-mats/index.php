<h1>Notes on Large Mats</h1>
<p>We buy our mat stock in rolls that are 20" wide. (24" for the clear mats.)</p>
<p>So, if the smaller of your two mat dimensions is more than 20" (or 24 in the case of clear mats), then we will have to ship that mat as two pieces.</p>
<p>If you are ordering this mat for a drawer that will contain a custom insert, then we can cut the two pieces so they will join beneath one of the half inch fixed walls.  Or if you know how you want the two pieces of a large mat to be cut, we will follow your instructions.</p>
<p>So, if you want to get your large mat pieces cut to your specs or cut to hide beneath an insert ordered separately, you must contact us immediately after you complete your order.  Otherwise, we will use our own judgement in sizing the two pieces.</p>

