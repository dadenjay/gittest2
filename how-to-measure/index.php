<h1>How to Measure</h1>
<p><strong>Drawer Inside Width</strong></p>
<p>Measure in several places, looking for the minimum value.  It is okay for your measurement to be &#8220;tight&#8221;.  We will build your inserts and mats 1/8&#8243; less than the width you tell us (unless specified otherwise).</p>
<p><a href="http://orderlydrawer.com/wp-content/uploads/2008/11/typ-drawer-width-measure-cleaned-up.jpg"><img class="aligncenter size-full wp-image-151" title="typ-drawer-width-measure-cleaned-up" src="http://orderlydrawer.com/wp-content/uploads/2008/11/typ-drawer-width-measure-cleaned-up.jpg" alt="" width="500" height="336" /></a></p>
<p><strong>Drawer Inside Depth</strong></p>

<p>Measure in several places, just like the width, looking for the smallest depth value.</p>
<p>You may notice that your drawer handle screws protrude about 1/8&#8243; at the front of the drawer.  For this reason, we build your full-depth inserts and mats 1/4&#8243; less than your tightest depth measurement.</p>
<p><a href="http://orderlydrawer.com/wp-content/uploads/2008/11/typ-drawer-depth-measure-cleaned-up.jpg"><img class="aligncenter size-full wp-image-150" title="typ-drawer-depth-measure-cleaned-up" src="http://orderlydrawer.com/wp-content/uploads/2008/11/typ-drawer-depth-measure-cleaned-up.jpg" alt="" width="500" height="325" /></a></p>
<p><strong>Drawer Inside Height</strong></p>
<p>Confirm that your drawer has at least 2 inches of internal height.  Our inserts are 2&#8243; high unless specified otherwise.</p>
<p><a href="http://orderlydrawer.com/wp-content/uploads/2008/11/typ-drawer-height-measure-clean-up.jpg"><img class="aligncenter size-full wp-image-152" title="typ-drawer-height-measure-clean-up" src="http://orderlydrawer.com/wp-content/uploads/2008/11/typ-drawer-height-measure-clean-up.jpg" alt="" width="500" height="462" /></a></p>

