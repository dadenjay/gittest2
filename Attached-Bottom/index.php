<h1>Does Your Drawer Organizer Need an Attached Bottom?</h1>
<p>Considering an attached bottom for your custom drawer insert?</p>
<p><em>First consider your drawer bottom.</em> If your drawer bottom is bowed, you need an attached bottom for your drawer insert.  Check to see if your drawer bottom is bowed by using a tight string or a straight edge to test it.</p>
<p>Especially on larger drawers, we recommend that you put some weight in the drawer so that you know whether the bottom will actually remain flat when loaded.</p>
<p><em>Next, consider what you&#8217;ll be storing in your drawer organizer.</em> Thin and/or very pointy utensils can be prone to getting wedged underneath a wall and/or movable divider.  If you&#8217;ll be storing these types of items, we recommend an attached bottom.</p>

<p><em>Other notes:</em></p>
<p>When we ship bottomless custom drawer inserts, we include four small wedges that can be used between the insert&#8217;s four corners and the walls of your drawer to hold the insert in place in your drawer.  The wedges also provide downward force on the frame of the insert, pinning it to the drawer bottom.</p>
<p>A rubber mat can provide additional coverage if gaps are present.</p>
<p>An attached bottom will add approximately 1/4&#8243; of height to your drawer insert.</p>

