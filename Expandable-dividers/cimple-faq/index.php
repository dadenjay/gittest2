<h1>Cimple FAQ</h1>
<p><strong>What is the purpose of the little rubber things in the feet grooves?</strong> These are little pivots.  They make the feet grip your drawer sides even if the sides of your drawer are not perfectly square.  Also, they help distribute the pressure/tension all along the feet, giving a firm, strong hold.  By compressing the rubber pivot with the divider walls and then locking in that tension with the cap, you&#8217;re getting a really stable fit.</p>
<p><strong>I want to use your dividers on a shelf, but the shelf height is less than 12&#8243; high.  How can I make the divider fit?</strong> You&#8217;ll need to take the long 10&#8243; wall piece out of the divider unit.  Using a box cutter-type knife,nick the cap to the length you need for your shelf divider.  Now assemble the divider by overlapping the two short walls.</p>

<p><strong>How do I install the Cimple divider in a drawer? </strong> We send detailed installation instructions with all orders.  If you have trouble, you can <a href="../install-cimple">view our installation video</a>.</p>

